def main():
    """
    Головна функція програми.
    Зчитує дані, формує зубчатий масив та виводить його.
    """
    
    # 1. Введення розміру квадратної матриці
    try:
        size = int(input("Введіть розмір квадратної матриці: "))
    except ValueError:
        print("Помилка: Необхідно ввести ціле число.")
        sys.exit(1)

    # 2. Введення символу-заповнювача
    filler = input("Введіть символ-заповнювач: ")

    # Перевірка: чи введено рівно один символ
    if len(filler) != 1:
        print("Помилка: Необхідно ввести рівно один символ.")
        sys.exit(1)

    # 3. Формування зубчатого списку (list of lists)
    # Згідно варіанту 12: заповнені верхній лівий та нижній правий кути.
    
    matrix = []
    half = size // 2  # Визначаємо середину матриці

    for i in range(size):
        row = []
        for j in range(size):
            # Логіка заповнення:
            # Якщо ми у верхній половині рядків (i < half) І у лівій половині стовпців (j < half)
            if i < half and j < half:
                row.append(filler)
            
            # Якщо ми у нижній половині рядків (i >= half) І у правій половині стовпців (j >= half)
            elif i >= half and j >= half:
                row.append(filler)
                
            # В інших випадках - пробіл (щоб зберегти форму матриці)
            else:
                row.append(" ")
        
        # Додаємо сформований рядок у матрицю
        matrix.append(row)

    # 4. Виведення результату
    print("\nРезультат:")
    for row in matrix:
        # ''.join(row) склеює список символів у один рядок
        print("".join(row))

# Перевірка, що файл запущено як основну програму
if __name__ == "__main__":
    main()